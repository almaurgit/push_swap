int			ft_factorial(int nb)
{
	int res;

	res = nb;
	if (nb == 0)
		return (1);
	if (nb < 0 || nb >= 13)
		return (0);
	while (nb > 1)
	{
		nb--;
		res *= nb;
	}
	return (res);
}
